/**
 * 简单的fetch包装（支持http/https）
 */
function fetch(url, options = {}) {
  return new Promise((resolve, reject) => {
    const lib = url.startsWith('https') ? https : http;
    const reqOptions = {
      headers: {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36',
        'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8',
        'Accept-Language': 'zh-CN,zh;q=0.9',
        ...options.headers,
      },
      timeout: 15000,
    };
    const req = lib.get(url, reqOptions, (res) => {
      // 处理重定向
      if (res.statusCode >= 300 && res.statusCode < 400 && res.headers.location) {
        fetch(res.headers.location, options).then(resolve).catch(reject);
        return;
      }
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => resolve({ status: res.statusCode, data }));
    });
    req.on('error', reject);
    req.on('timeout', () => { req.destroy(); reject(new Error('Request timeout')); });
  });
}

/**
 * 从HTML中提取正文内容
 */
function extractContent(html, selector = 'article, .article-content, .content, #content, main, .post-content') {
  const $ = cheerio.load(html);
  // 移除无关元素
  $('script, style, nav, header, footer, aside, .sidebar, .ad, .advertisement, .comments, .related, .nav, .menu, .breadcrumb').remove();

  let content = '';
  const mainContent = $(selector);
  if (mainContent.length > 0) {
    content = mainContent.html() || '';
  } else {
    content = $('body').html() || '';
  }

  return cheerio.load(content, { decodeEntities: false }).html()
    .replace(/<[^>]+>/g, (match) => {
      // 保留换行和必要标签
      if (/^(br|p|div|h[1-6]|ul|ol|li|table|tr|td|th|pre|code)$/i.test(match.replace(/<\/?/, '').replace(/[^a-z]/gi, ''))) return '\n';
      return ' ';
    })
    .replace(/&nbsp;/g, ' ')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&amp;/g, '&')
    .replace(/&quot;/g, '"')
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}

/**
 * 向API提交词条
 */
async function submitEntry(entry) {
  try {
    const res = await axios.post(API_BASE, entry);
    return { success: true, id: res.data?.data?.id || res.data?.id };
  } catch (e) {
    return { success: false, error: e.message };
  }
}

/**
 * 生成Markdown文件
 */
function generateMarkdown(entries) {
  let md = `# 诛仙世界游戏攻略知识库\n\n`;
  md += `> 生成时间: ${new Date().toLocaleString('zh-CN')}\n`;
  md += `> 共收录 ${entries.length} 篇攻略\n\n`;
  md += `---\n\n`;

  const byCategory = {};
  entries.forEach(e => {
    if (!byCategory[e.category]) byCategory[e.category] = [];
    byCategory[e.category].push(e);
  });

  for (const [cat, items] of Object.entries(byCategory)) {
    md += `## ${cat}\n\n`;
    for (const item of items) {
      md += `### ${item.title}\n\n`;
      if (item.summary) md += `**摘要**: ${item.summary}\n\n`;
      if (item.tags) md += `**标签**: ${item.tags}\n\n`;
      md += `${item.content}\n\n`;
      md += `---\n\n`;
    }
  }

  return md;
}

